public class Karte
{
    String bez;
    String farbe;
    int zahl;
    public void setBez(String pBez)
    {
        bez = pBez;
    }
    
    public String getBez()
    {
        return bez;
    }
    
    public void setFarbe(String pFarbe)
    {
        farbe = pFarbe;
    }
    
    public String getFarbe()
    {
        return farbe;
    }
    
    public void setZahl(int pZahl)
    {
        zahl = pZahl;
    }
    
    public int getZahl()
    {
        return zahl;
    }
}